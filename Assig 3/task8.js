// Counting
for (var i = 1; i < 16; i++ ){
console.log(i);
}

// Reverse Counting
for (var i = 10; i >= 1; i--){
    console.log(i);
}

// Even Series
for (var i = 0; i < 11; i++ ){
console.log(i*2);
}

//Odd Series
for (var i = 1; i < 20; i++ ){
if (i % 2 != 0){
    console.log(i);
}
}

// Series with k alphabet
for (var i = 1; i < 11; i++ ){
console.log(i*2 + "k");
}
